//! 李群/李代数模块 - 李群驱动的分布式上下文验证系统
//!
//! # 架构设计
//!
//! 本模块实现李群驱动的信任根上移架构，将信任从节点层上移到链上聚合公式。
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────────┐
//! │  第一层：分布式上下文分片层 (不可信节点)                         │
//! │  • ContextShardManager (已实现)                                  │
//! │  • ProviderLayerManager (已实现)                                 │
//! │  • LieAlgebraMapper (李代数映射器) ← 本模块                      │
//! └─────────────────────────────────────────────────────────────────┘
//!                               ↓ 提交 A_i
//! ┌─────────────────────────────────────────────────────────────────┐
//! │  第二层：李群链上聚合层 (系统核心，信任根)                       │
//! │  • PBFTConsensus (已实现共识框架)                                │
//! │  • Blockchain (已实现存证链)                                     │
//! │  • LieGroupAggregator (李群聚合器) ← 信任根                      │
//! └─────────────────────────────────────────────────────────────────┘
//!                               ↓ 生成 G
//! ┌─────────────────────────────────────────────────────────────────┐
//! │  第三层：QaaS 质量验证层 (李群度量)                              │
//! │  • QaaSService (已实现)                                          │
//! │  • QualityAssessor (已实现)                                      │
//! │  • LieGroupMetric (李群度量器)                                   │
//! └─────────────────────────────────────────────────────────────────┘
//!                               ↓ 输出 proof
//! ┌─────────────────────────────────────────────────────────────────┐
//! │  第四层：区块链存证与激励层                                      │
//! │  • Blockchain + KvCacheProof (已实现)                            │
//! │  • ValidatorReputation (已实现)                                  │
//! │  • 扩展：支持李代数/李群承诺上链                                 │
//! └─────────────────────────────────────────────────────────────────┘
//! ```
//!
//! # 核心创新点：信任根上移
//!
//! - **旧架构**：节点 → 哈希校验 → 上链存证 → 共识仲裁（信任根在节点）
//! - **新架构**：节点 → 提交局部 A_i → 链上李群聚合 G → QaaS 验证（信任根在聚合公式）
//!
//! # 关键洞察
//!
//! - 节点只提交局部李代数 A_i = to_algebra(h_i)
//! - 全局李群状态 G = exp(1/N * Σlog(g_i)) 在链上生成
//! - 任何局部篡改 → 聚合后距离暴增（×5.47，实验验证）
//!
//! # 模块结构
//!
//! - [`types`]: 核心数据结构（LieAlgebraElement, LieGroupElement）
//! - [`mapper`]: 李代数映射器 Trait 和实现
//! - [`aggregator`]: 李群聚合器（信任根）
//! - [`metric`]: 李群度量器

